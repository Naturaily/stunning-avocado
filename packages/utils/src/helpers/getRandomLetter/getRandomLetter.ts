import { getRandomNumber } from '../getRandomNumber';

const alphabet = [
  'a',
  'b',
  'c',
  'd',
  'e',
  'f',
  'g',
  'h',
  'i',
  'j',
  'k',
  'l',
  'm',
  'n',
  'o',
  'p',
  'q',
  'r',
  's',
  't',
  'u',
  'v',
  'w',
  'x',
  'y',
  'z',
] as const;

/**
 * It returns a random letter from the alphabet array
 */
export const getRandomLetter = () => alphabet[getRandomNumber(0, alphabet.length - 1)];
